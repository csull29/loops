 public class OutputUsingWhileLoop{
     public static void main(String args[]){
		// while loop
        int i=50;
		while (i>=0){
			//Outputing all numbers between 50 and 0 counting down in 5's
			System.out.println(i);
			i=i-5;
		}
     }
 }